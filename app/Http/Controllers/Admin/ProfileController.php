    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */

    public function update(UpdateProfileRequest $request, $id)
    {
        
        if ($request->hasfile('image')) {
            $file = $request->file('image');
            $extension = $file->getClientOriginalExtension(); // getting image extension
            $filename = time() . '.' . $extension;
            $file->move(public_path('assets/images/avtar/'), $filename);
            $image = 'assets/images/avtar/' . $filename;
        }
        $user = auth()->user();

        $user->update($request->validated()+['image'=>$image]);

        return redirect()->route('admin.profile.index')->with('message', __('global.update_profile_success'));
    }

    /**
     * Update the specified resource in storage.
